function onEventFinish(player,csid,option)
--printf("CSID: %u",csid);
--printf("RESULT: %u",option);
    if (csid == 0x010) then
        player:setVar("UnderOathCS", 2)  -- Quest: Under Oath - PLD AF3
    end
end;




